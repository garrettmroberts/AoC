const formatData = input => {
  const separatedInputs = input.split('\n');
  return separatedInputs.slice(1,-1);
}

const calculateRPSScore = (input) => {
  const data = formatData(input);
  let score = 0;
  data.forEach((rpsRound) => {
    const opponentHand = rpsRound.split(' ')[0];
    const myHand = rpsRound.split(' ')[1];
    if (opponentHand === 'A') {
      if (myHand === 'X') {
        score += 4;
      } else if (myHand === 'Y') {
        score += 8;
      } else {
        score += 3;
      }
    } else if (opponentHand === 'B') {
      if (myHand === 'X') {
        score += 1;
      } else if (myHand === 'Y') {
        score += 5;
      } else {
        score += 9;
      }
    } else {
      if (myHand === 'X') {
        score += 7;
      } else if (myHand === 'Y') {
        score += 2;
      } else {
        score += 6;
      }
    }
  });
  return score;
};

const calculateRPSScoreV2 = input => {
  const data = formatData(input);
  let score = 0;
  data.forEach((rpsRound) => {
    const opponentHand = rpsRound.split(' ')[0];
    const gameOutcome = rpsRound.split(' ')[1];
    if (opponentHand === 'A') { // opponent plays rock
      if (gameOutcome === 'X') {
        score += 3; // lose w/ scissors
      } else if (gameOutcome === 'Y') {
        score += 4; // tie w/ rock
      } else {
        score += 8; // win w/ paper
      }
    } else if (opponentHand === 'B') { // opponent plays paper
      if (gameOutcome === 'X') {
        score += 1; // lose w/ rock
      } else if (gameOutcome === 'Y') {
        score += 5; // tie w/ paper
      } else {
        score += 9; // win w/ scissors
      }
    } else { // opponent plays scissors
      if (gameOutcome === 'X') {
        score += 2; // lose w/ paper
      } else if (gameOutcome === 'Y') {
        score += 6; // tie w/ scissors
      } else {
        score += 7; // win w/ rock
      }
    }
  });
  return score;
}

